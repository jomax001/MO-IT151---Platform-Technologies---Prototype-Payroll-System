if (LoginController.login(username, password, role)) {
    JOptionPane.showMessageDialog(this, "Login successful!");
    this.dispose();
    new NewHRDashboard().setVisible(true); // or role-based dashboard
} else {
    JOptionPane.showMessageDialog(this, "Invalid credentials");
}
